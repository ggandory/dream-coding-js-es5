// Function (함수)
// - fundamental building block in the program
// - subprogram can be used multiple times
// - performs a task or calculates a value(값을 계산하기 위해서 존재)

// 1. Function declaration(함수 정의 방법)
// function name(param1, param2) { body... return; }
// one function === one thing(하나의 함수는 하나의 일만 하도록 만든다.)
// naming: doSomething, command, verb(명령, 동사형태로 만들어야함 함수 이름은)
// e.g. createCardAndPoint -> createCard, createPoint(예시들)
// function is object in JS(자바에서 함수는 오브젝트이다.)
function printHello() {
  console.log('Hello');
}
printHello();

function log(message) {
  console.log(message);
}
log('Hello@');
